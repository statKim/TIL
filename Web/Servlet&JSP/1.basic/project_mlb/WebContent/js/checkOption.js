$('#pitTeam').click(function() {
	var ischecked = $('#pitTeam').prop('checked');
		 
	if(ischecked){
		$("#pitTeamVal").removeAttr("disabled");
	}else{
		$('#pitTeamVal').prop('disabled',true);			
	}
});

